bool good(ll a, ll n){
    // cout<<"a - "<<a<<'\n';,
    if(a&1){
        ll j = (a+1)/2;
        // cout<<"j - "<<j<<'\n';
        if(4*j*j >= n)
        return true;
    }
    else{
        ll j = a/2;
        if(4*(j)*(j+1)+1 >= n) return true;
    }
    return false;
}
